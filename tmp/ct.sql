CREATE TABLE IF NOT EXISTS corp.store (
    store_id INTEGER NOT NULL, 
    store_type VARCHAR(255), 
    region_id INTEGER, 
    store_name VARCHAR(255), 
    store_number INTEGER, 
    store_street_address VARCHAR(255), 
    store_city VARCHAR(255), 
    store_state VARCHAR(255), 
    store_postal_code VARCHAR(255), 
    store_country VARCHAR(255), 
    store_manager VARCHAR(255), 
    store_phone VARCHAR(255), 
    store_fax VARCHAR(255), 
    first_opened_date DATE, 
    last_remodel_date DATE, 
    store_sqft INTEGER, 
    grocery_sqft INTEGER, 
    frozen_sqft INTEGER, 
    meat_sqft INTEGER, 
    coffee_bar BOOLEAN, 
    video_store BOOLEAN, 
    salad_bar BOOLEAN, 
    prepared_food BOOLEAN, 
    florist BOOLEAN,
    CONSTRAINT store_id_pk PRIMARY KEY (store_id));
